# Ejercicio 1. Escribe un programa que pida al usuario un número entero y determine
# si es positivo o negativo. El programa debe imprimir un mensaje indicando el resultado.


numero = int((input("Introduce un número entero: ")))

if numero >= 0:
    print(f"El número {numero} es positivo.")
else:
    print(f"El número {numero} es negativo.")




